window.addEventListener('keydown', function(e) {
    if (e.key == '.') {
        document.getElementById("coffinDance").play();
    }
});
window.addEventListener('keyup', function(e) {
    if (e.key == '.') {
        document.getElementById("coffinDance").pause();
    }
    updateDate();
});
/*
    ==============================================================
            End of Coffin Dance Meme
    ==============================================================
*/
